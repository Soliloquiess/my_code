char	*ft_strcat(char *dest, char *src)
{
	int i;
	int s_i;

	i = 0;
	s_i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[s_i] != '\0')
	{
		dest[i + s_i] = src[s_i];
		s_i++;
	}
	dest[i + s_i] = '\0';
	return (dest);
}
